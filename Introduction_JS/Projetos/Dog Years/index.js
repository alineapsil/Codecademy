let myAge = 26;
// This is my age.
let earlyYears = 2;
// The first years of a dog.
earlyYears *= 10.5;
laterYears = myAge - 2;
// Aqui tiramos os dois anos contados acima, e agora trabalharemos com os outros anos.
laterYears *= 4;
// Os outros anos equivalem a 4 do nosso.
myAgeInDogYears = earlyYears + laterYears;
// Minha idade em "anos de cachorro".
myName = "Aline".toLowerCase();
// Retorna me nome com todas em minusculas.
console.log(
  `My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years`
);
